function toBase64(content: string): string {
  return Buffer.from(content, 'utf8').toString('base64')
}

// Normalizes each component's export shape ONCE and reuses that exact text for both the raw
// src/components/*.jsx (which the hand-authored *.stories.jsx statically import as named
// exports — Storybook/Rollup builds fail otherwise) and the dist/* bundle, so the two never
// disagree about what a component actually exports.
async function buildDistFiles(
  colors: Record<string, string>,
  colorScales: Record<string, unknown> | null,
  typography: Record<string, unknown>,
  componentCode: string,
  additionalComponents: AdditionalComponents,
): Promise<{ cjs: string; esm: string; css: string; sources: ComponentSources }> {
  const rawSources: ComponentSources = {
    Button: componentCode,
    Input: additionalComponents.input,
    Textarea: additionalComponents.textarea,
    Alert: additionalComponents.alert,
    Badge: additionalComponents.chip,
  }
  const sources = normalizeComponentSources(rawSources)
  const themeExtend = scaffold.computeTailwindThemeExtend(colors, colorScales, typography)

  const [{ cjs, esm }, css] = await Promise.all([
    buildComponentBundles(sources),
    buildComponentCss(sources, themeExtend),
  ])

  return { cjs, esm, css, sources }
}

// ─── OAuth ───────────────────────────────────────────────────────────────────

export async function exchangeCodeForToken(
  code: string,
): Promise<{ accessToken: string; login: string }> {
  const tokenRes = await fetch('https://github.com/login/oauth/access_token', {
    method: 'POST',
    headers: { Accept: 'application/json', 'Content-Type': 'application/json' },
    body: JSON.stringify({
      client_id: process.env.GITHUB_CLIENT_ID,
      client_secret: process.env.GITHUB_CLIENT_SECRET,
      code,
    }),
  })

  const tokenData = (await tokenRes.json()) as { access_token?: string; error?: string }
  if (!tokenData.access_token) throw new Error(tokenData.error ?? 'OAuth exchange failed')

  const userRes = await fetch('https://api.github.com/user', {
    headers: {
      Authorization: `Bearer ${tokenData.access_token}`,
      Accept: 'application/vnd.github.v3+json',
    },
  })
  const user = (await userRes.json()) as { login: string }

  return { accessToken: tokenData.access_token, login: user.login }
}

export async function saveGithubConnection(
  userId: string,
  accessToken: string,
  login: string,
): Promise<void> {
  const encryptedToken = encrypt(accessToken)
  await prisma.githubConnection.upsert({
    where: { userId },
    update: { accessToken: encryptedToken, githubUsername: login, revokedAt: null },
    create: { userId, accessToken: encryptedToken, githubUsername: login, scope: 'repo' },
  })
}

export async function getDecryptedToken(
  userId: string,
): Promise<{ token: string; login: string } | null> {
  const connection = await prisma.githubConnection.findUnique({ where: { userId } })
  if (!connection || connection.revokedAt) return null
  return { token: decrypt(connection.accessToken), login: connection.githubUsername }
}

export async function getConnectionStatus(
  userId: string,
): Promise<{ connected: boolean; username?: string }> {
  const connection = await prisma.githubConnection.findUnique({ where: { userId } })
  if (!connection || connection.revokedAt) return { connected: false }
  return { connected: true, username: connection.githubUsername }
}

// ─── Repository operations ───────────────────────────────────────────────────

export async function createRepository(
  token: string,
  name: string,
  isPrivate: boolean,
): Promise<{ fullName: string }> {
  const octokit = new Octokit({ auth: token })
  try {
    const { data } = await octokit.repos.createForAuthenticatedUser({
      name,
      private: isPrivate,
      description: 'Design system generated by SuperDSAI Generator',
      auto_init: false,
    })
    return { fullName: data.full_name }
  } catch (err: unknown) {
    if ((err as { status?: number }).status === 422) {
      throw new RepoConflictError(`${name}-2`)
    }
    throw err
  }
}

export async function scaffoldRepository(
  token: string,
  fullName: string,
  colors: Record<string, string>,
  typography: Record<string, unknown>,
  colorScales: Record<string, unknown> | null,
  typographyScale: unknown[] | null,
  componentCode: string,
  additionalComponents: AdditionalComponents,
  repoName: string,
): Promise<void> {
  const octokit = new Octokit({ auth: token })
  const [owner, repo] = fullName.split('/')

  const { cjs, esm, css, sources } = await buildDistFiles(colors, colorScales, typography, componentCode, additionalComponents)

  const files: { path: string; content: string }[] = [
    { path: 'package.json', content: scaffold.buildPackageJson(repoName) },
    { path: '.gitignore', content: scaffold.buildGitignore() },
    { path: '.storybook/main.js', content: scaffold.buildStorybookMain() },
    { path: '.storybook/preview.js', content: scaffold.buildStorybookPreview() },
    { path: 'src/index.css', content: scaffold.buildIndexCss() },
    { path: 'src/tokens/colors.json', content: scaffold.buildColorsJson(colors) },
    { path: 'src/tokens/typography.json', content: scaffold.buildTypographyJson(typography) },
    { path: 'src/tokens/colorScales.json', content: scaffold.buildColorScalesJson(colorScales) },
    { path: 'src/tokens/typographyScale.json', content: scaffold.buildTypographyScaleJson(typographyScale) },
    { path: 'tailwind.config.js', content: scaffold.buildTailwindConfig(colors, colorScales) },
    { path: 'tailwind-preset.js', content: scaffold.buildTailwindPreset(colors, colorScales, typography) },
    { path: 'postcss.config.js', content: scaffold.buildPostcssConfig() },
    { path: 'vercel.json', content: scaffold.buildVercelConfig() },
    { path: 'src/index.js', content: scaffold.buildIndexEntry() },
    { path: 'dist/index.js', content: cjs },
    { path: 'dist/index.esm.js', content: esm },
    { path: 'dist/index.css', content: css },
    { path: 'README.md', content: scaffold.buildReadme(repoName) },
    { path: 'src/components/Button.jsx', content: sources.Button },
    { path: 'src/components/Button.stories.jsx', content: scaffold.buildButtonStories() },
    { path: 'src/components/Input.jsx', content: sources.Input },
    { path: 'src/components/Input.stories.jsx', content: scaffold.buildInputStories() },
    { path: 'src/components/Alert.jsx', content: sources.Alert },
    { path: 'src/components/Alert.stories.jsx', content: scaffold.buildAlertStories() },
    { path: 'src/components/Textarea.jsx', content: sources.Textarea },
    { path: 'src/components/Textarea.stories.jsx', content: scaffold.buildTextareaStories() },
    { path: 'src/components/Badge.jsx', content: sources.Badge },
    { path: 'src/components/Badge.stories.jsx', content: scaffold.buildBadgeStories() },
    { path: 'src/foundations/Foundations.stories.jsx', content: scaffold.buildFoundationsStory() },
  ]

  for (const file of files) {
    await octokit.repos.createOrUpdateFileContents({
      owner,
      repo,
      path: file.path,
      message: 'chore: initial design system scaffold',
      content: toBase64(file.content),
    })
  }
}

export async function createUpdatePR(
  token: string,
  fullName: string,
  colors: Record<string, string>,
  typography: Record<string, unknown>,
  colorScales: Record<string, unknown> | null,
  typographyScale: unknown[] | null,
  componentCode: string,
  additionalComponents: AdditionalComponents,
  dsName: string,
): Promise<{
  prNumber: number
  prUrl: string
  branchName: string
  prTitle: string
  prBody: string
}> {
  const octokit = new Octokit({ auth: token })
  const [owner, repo] = fullName.split('/')

  const { data: mainBranch } = await octokit.repos.getBranch({ owner, repo, branch: 'main' })
  const mainSha = mainBranch.commit.sha
  const branchName = `ds-update-${Date.now()}`

  await octokit.git.createRef({ owner, repo, ref: `refs/heads/${branchName}`, sha: mainSha })

  const { cjs, esm, css, sources } = await buildDistFiles(colors, colorScales, typography, componentCode, additionalComponents)

  const filesToUpdate = [
    { path: 'package.json', content: scaffold.buildPackageJson(repo) },
    { path: 'src/tokens/colors.json', content: scaffold.buildColorsJson(colors) },
    { path: 'src/tokens/typography.json', content: scaffold.buildTypographyJson(typography) },
    { path: 'src/tokens/colorScales.json', content: scaffold.buildColorScalesJson(colorScales) },
    { path: 'src/tokens/typographyScale.json', content: scaffold.buildTypographyScaleJson(typographyScale) },
    { path: 'tailwind.config.js', content: scaffold.buildTailwindConfig(colors, colorScales) },
    { path: 'tailwind-preset.js', content: scaffold.buildTailwindPreset(colors, colorScales, typography) },
    { path: '.gitignore', content: scaffold.buildGitignore() },
    { path: 'src/index.js', content: scaffold.buildIndexEntry() },
    { path: 'dist/index.js', content: cjs },
    { path: 'dist/index.esm.js', content: esm },
    { path: 'dist/index.css', content: css },
    { path: 'README.md', content: scaffold.buildReadme(repo) },
    { path: 'src/components/Button.jsx', content: sources.Button },
    { path: 'src/components/Input.jsx', content: sources.Input },
    { path: 'src/components/Input.stories.jsx', content: scaffold.buildInputStories() },
    { path: 'src/components/Alert.jsx', content: sources.Alert },
    { path: 'src/components/Alert.stories.jsx', content: scaffold.buildAlertStories() },
    { path: 'src/components/Textarea.jsx', content: sources.Textarea },
    { path: 'src/components/Textarea.stories.jsx', content: scaffold.buildTextareaStories() },
    { path: 'src/components/Badge.jsx', content: sources.Badge },
    { path: 'src/components/Badge.stories.jsx', content: scaffold.buildBadgeStories() },
    { path: 'src/foundations/Foundations.stories.jsx', content: scaffold.buildFoundationsStory() },
  ]

  for (const file of filesToUpdate) {
    let currentSha: string | undefined
    try {
      const { data: existing } = await octokit.repos.getContent({
        owner, repo, path: file.path, ref: branchName,
      })
      if ('sha' in existing) currentSha = existing.sha
    } catch { /* new file */ }

    await octokit.repos.createOrUpdateFileContents({
      owner, repo,
      path: file.path,
      message: `update: ${file.path}`,
      content: toBase64(file.content),
      branch: branchName,
      ...(currentSha ? { sha: currentSha } : {}),
    })
  }

  const { title: prTitle, body: prBody } = await generatePRDescription({
    changedTokenKeys: Object.keys(colors),
    hasComponentChanges: true,
    dsName,
  })

  const { data: pr } = await octokit.pulls.create({
    owner, repo, title: prTitle, body: prBody, head: branchName, base: 'main',
  })

  return { prNumber: pr.number, prUrl: pr.html_url, branchName, prTitle, prBody }
}
